pub fn vec_to_ptr<T>(values: Vec<T>) -> (*mut T, u32) {
    let len = values.len();
    let boxed = values.into_boxed_slice();
    let ptr = Box::into_raw(boxed) as *mut _;

    (ptr, len as u32)
}
